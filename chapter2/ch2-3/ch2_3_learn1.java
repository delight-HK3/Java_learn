public class ch2_3_learn1 {
    public static void main(String[] args) {
        float flo1 = 85.7f;
        int in = (int)flo1;
        System.out.println(in);//85
        //형 변환을 했기 때문에 정수부분인 85만 in에 저장
        System.out.printf("%c%n",(char)70);
        System.out.printf("%d%n",(int)'F');
    }
}
