public class ch3_5_learn1 {
    public static void main(String[] args) {
        for(int i = 0; i < 5; i++){
            // int i 는 초기화
            // i < 5 는 조건식
            // i++ 은 증감식
            System.out.println(i);
            // i에 1씩 더하여 값을 출력시킨뒤 i의 값이 5가 되면
            // 조건식을 적용한 결과 false가 되기 때문에 for문을 나간다.
        }
    }
}
